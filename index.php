<br>The relevance of our design to the theme is that for example in most movies, when someone has gone through so much and has lost faith in a brighter future, a person will offer their hand to them, giving them "HOPE". Hope is a desire, an expectation for what you would want to happen. But that desire is all too easy to fade away, without something or someone constantly giving it to you. That is why we decided to make a hand reaching out, and a hand wanting to take that invitation, that invitation with no certainty, but full of hope. You can tell by first glance, despite it being a lifeless drawing. The want, the desire, the desperation of that hand for something better, for something that can be found only after taking that out-reached hand. And that, to us, is what hope feels like.

